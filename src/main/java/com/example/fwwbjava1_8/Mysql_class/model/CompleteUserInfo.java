package com.example.fwwbjava1_8.Mysql_class.model;

public class CompleteUserInfo {

    private Integer id;
    private Integer age;
    private Integer income;
    private Integer defaulter;
    private Integer working_status;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public Integer getIncome() {
        return income;
    }

    public void setIncome(Integer income) {
        this.income = income;
    }

    public Integer getDefaulter() {
        return defaulter;
    }

    public void setDefaulter(Integer defaulter) {
        this.defaulter = defaulter;
    }

    public Integer getWorking_status() {
        return working_status;
    }

    public void setWorking_status(Integer working_status) {
        this.working_status = working_status;
    }
}
